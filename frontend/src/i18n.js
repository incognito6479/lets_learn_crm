const currentLanguage = ref(localStorage.getItem('lang') || 'ru')

export const i18n = {
  get locale() {
    return currentLanguage.value
  },
  set locale(lang) {
    if (translations[lang]) {
      currentLanguage.value = lang
      localStorage.setItem('lang', lang)
    }
  },
  t(key, params = {}) {
    const keys = key.split('.')
    let currentObj = translations[currentLanguage.value]
    
    for (const k of keys) {
      if (currentObj && currentObj[k] !== undefined) {
        currentObj = currentObj[k]
      } else {
        // Fallback to Russian
        let fallbackObj = translations['ru']
        for (const fk of keys) {
          if (fallbackObj && fallbackObj[fk] !== undefined) {
            fallbackObj = fallbackObj[fk]
          } else {
            fallbackObj = null
            break
          }
        }
        return fallbackObj !== null ? fallbackObj : key
      }
    }
    
    let text = currentObj
    if (typeof text === 'string') {
      Object.keys(params).forEach(p => {
        text = text.replace(new RegExp(`{${p}}`, 'g'), params[p])
      })
    }
    return text
  }
}

export default {
  install(app) {
    app.config.globalProperties.$t = (key, params) => i18n.t(key, params)
    app.config.globalProperties.$i18n = i18n
  }
}
